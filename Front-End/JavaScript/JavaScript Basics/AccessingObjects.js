//Creating a person Object
const person = {
    firstname : "Sneha",
    lastname : "Gangapuram"
}

//Accessing the properties of the dot notation
console.log("First name with dot notation " + person.firstname)
console.log("Last name with dot notation " + person.lastname)

//Accessing the properties of the bracket notation
console.log("First name with bracket notation " + person["firstname"])
console.log("Last name with bracket notation " + person["lastname"])
