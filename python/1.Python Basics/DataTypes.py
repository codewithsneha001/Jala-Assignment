#  Data Types int, Boolean, char, float, double 
num = 10                 # int
is_true = True           # bool
grade = 'A'              # char (actually just a 1-character string in Python)
percentage = 85.5        # float
pi = 3.14159265359       # double (Python uses float for both float and double precision)

print("Integer:", num)
print("Boolean:", is_true)
print("Character:", grade)
print("Float:", percentage)
print("Double (float in Python):", pi)
