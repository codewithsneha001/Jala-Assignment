# Different ways creating a string
# Using single quotes
str1 = 'Hello'

# Using double quotes
str2 = "World"

# Using triple quotes (also used for multiline strings)
str3 = '''Python Programming'''

# From a list of characters
str4 = ''.join(['P', 'y', 't', 'h', 'o', 'n'])

print(str1, str2, str3, str4)
